<?php
session_start();
require_once 'config.php'; 

// бһҶбҹӮбһҖбһҳбһҫбһӣбһҗбһ¶бһҸбһҫбһ–бһ·бһҸбһҮбһ¶бһҳбһ¶бһ“бһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһ”бһүбҹ’бһҮбһјбһ“бһҳбһҖбһҸбһ¶бһҳбһҡбһҷбҹҲ Form бһҳбҹӮбһ“бһ¬бһўбһҸбҹӢ (бһҖбһ¶бһҡбһ–бһ¶бһҡбһўбҹ’бһ“бһҖбһӣбһҪбһ…бһ…бһјбһӣбһ•бҹ’бһ‘бһ¶бһӣбҹӢ)
if ($_SERVER["REQUEST_METHOD"] == "POST") {

    // бҹЎ. бһ…бһ¶бһ”бҹӢбһҷбһҖбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷ бһ“бһ·бһ„бһҹбһҳбҹ’бһўбһ¶бһҸбһҠбһҫбһҳбҹ’бһ”бһёбһҖбһ¶бһҡбһ–бһ¶бһҡбһҖбһ¶бһҡ Hack (SQL Injection)
    $email = $conn->real_escape_string(trim($_POST['email']));
    $type  = $conn->real_escape_string(trim($_POST['type']));
    $phone = $conn->real_escape_string(trim($_POST['phone']));

    // бҹў. бһҹбһҡбһҹбҹҒбһҡбһҖбһјбһҠ SQL бһ”бһүбҹ’бһ…бһјбһӣбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷ
    // бһ…бҹҶбһҺбһ¶бҹҶбҹ– бһҷбһҫбһ„бһҳбһ·бһ“бһ”бһ¶бһ…бҹӢбһҹбһҡбһҹбҹҒбһҡбһ”бһүбҹ’бһ…бһјбһӣ status бһ“бһ·бһ„ created_at бһ‘бҹҒ бһ–бҹ’бһҡбҹ„бҹҮ Database бһңбһ¶бһ“бһ№бһ„бһҡбһ»бһүбһ–бһ¶бһҖбҹ’бһҷ 'unread' бһ“бһ·бһ„ бһҳбҹүбҹ„бһ„бһ–бһ·бһҸбһ”бҹ’бһҡбһ¶бһҖбһҠбһ…бһјбһӣбһҠбҹ„бһҷбһҹбҹ’бһңбҹҗбһҷбһ”бҹ’бһҡбһңбһҸбҹ’бһҸбһ·
    $sql = "INSERT INTO notifications (email, phone, type) 
            VALUES ('$email', '$phone', '$type')";

    // бҹЈ. бһ”бһүбҹ’бһҮбһ¶бһұбҹ’бһҷ Database бһ’бҹ’бһңбһҫбһҖбһ¶бһҡ
    if ($conn->query($sql) === TRUE) {
        // бһ–бҹҒбһӣ Save бһҮбҹ„бһӮбһҮбҹҗбһҷ бһ”бһүбҹ’бһҮбһјбһ“бһӮбһ¶бһҸбҹӢбһҸбҹ’бһҡбһӣбһ”бҹӢбһ‘бҹ…бһ‘бҹҶбһ–бҹҗбһҡ Login (index.php) бһңбһ·бһүбһҷбҹүбһ¶бһ„бһҹбҹ’бһ„бҹҖбһҳбһҹбҹ’бһ„бһ¶бһҸбҹӢ
        header("Location: index.php");
        exit();
    } else {
        // бһ”бһҫбһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶ Error бһҺбһ¶бһҳбһҪбһҷ бһңбһ¶бһ“бһ№бһ„бһӣбҹ„бһҸбһ”бҹ’бһҡбһ¶бһ”бҹӢбһ“бҹ…бһ‘бһёбһ“бҹҒбҹҮ
        echo "Error: " . $sql . "<br>" . $conn->error;
    }

} else {
    header("Location: index.php");
    exit();
}
?>